public class CalculadoraExamen {
	private int a;
	private int b;

	public CalculadoraExamen(int a, int b) {
    	this.a = a;
    	this.b = b;
	}

	public int getA() {
    	return a;
	}

	public void setA(int a) {
    	this.a = a;
	}

	public int getB() {
    	return b;
	}

	public void setB(int b) {
    	this.b = b;
	}

	public int sumar() {
    	return a + b;
	}

	public int restar() {
    	return a - b;
	}

	public int multiplicar() {
    	return a * b;
	}

	public int dividir() {
    	if (b == 0) {
        	throw new IllegalArgumentException("No se puede dividir por cero");
    	}
    	return a / b;
	}
}